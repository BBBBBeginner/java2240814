package ch05.lecture.p01array;

public class C01array {
    public static void main(String[] args) {
        int a;
        a = 3;
        System.out.println("a =" + a);

        a = 4;
        System.out.println("a =" + a);

        int[] b; // 하나에 한개 저장이 아닌 여러개 저장 가능
        b = new int[5];
        b[0] = 1;
        b[1] = 10;
        b[2] = 100;

        // 배열 (array) : 여러 값을 가지는 자료형(자료구조)


    }
}
