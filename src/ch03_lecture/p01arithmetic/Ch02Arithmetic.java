public class Ch02Arithmetic {
    public static void main(String[] args) {

        long e = 7l;
        double f = 3.14;
//        long g = e +f;
        double h = e + f;

        //나누기 (/) : 정수 끼리의 연산 결과는 정수
        System.out.println(10 / 3);//3
        // 나누기 (/) : 실수 끼리의 연산 결과는 실수
        System.out.println(10.0 / 3.0);//3.333
//        나누기 (/) : 정수 끼리의 연산 결과는 정수
        System.out.println(10 / 3);//3
    }
}
