package ch03_lecture.p06Assignment;

public class C02Assignment {
    public static void main(String[] args) {
        // = : 오른껒 값 왼쩍 변수에 대입
        // += : (왼쪽값과 오른쪽 값을 더해서) 왼쪽 변수에 대입
        // -= : (왼쪽 값에서 오른쪽값을 빼서) 왼쪽 변수에 대입
        // *= : (왼쪽 값에서 오른쪽값을 곱해서) 왼쪽 변수에 대입
        // /= : (왼쪽 값에서 오른쪽값을 나눠서) 왼쪽 변수에 대입
        // %= : (왼쪽 값에서 오른쪽값을 나눈 나머지를) 왼쪽 변수에 대입

        int a = 10;
        int b = 3;
        a += b; // a = a+b

        int c = 10;
        int d = 3;
        c -= d; // ch05_lecture.p01Array.a.c = ch05_lecture.p01Array.a.c-d

        int e = 10;
        int f = 3;
        e *= f; // e = e * f

        int g = 10;
        int h = 3;
        g /= h; //g = g / h

        int i = 10;
        int j = 3;
        i %= j; //i = i %j


    }
}
