package p06_lecture.CheckOut._20;

public class Account {

    static int number;
    static String name;
    static int money;
    static int out;
    static int in;


    public void setNumber(int number) {
        this.number = number;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public void setOut(int out) {
        this.out = out;
    }

    public void setIn(int in) {
        this.in = in;
    }
}
