package ch02.lectucre.variable;

public class Co2DoubleJava {
    public static void main(String[] args) {
        double b = 3.14;
        System.out.println(b);

        float c = 3.14F;
        double d = 3000000;
        System.out.println(d);
        double e = 3e6; //3 곱하기 10의 6승
        System.out.println(e);
        double f = 314e-2;
        System.out.println(f);
    }
}
